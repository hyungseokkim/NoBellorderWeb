package com.jang.bea.model;

public class Shop {

	private int shop_number;
	private float shop_x;
	private float shop_y;
	private String shop_name;
	private String shop_introduction;
	private String shop_logo;
	private int shop_grade;
	
	public int getShop_number() {
		return shop_number;
	}
	public void setShop_number(int shop_number) {
		this.shop_number = shop_number;
	}

	public float getShop_x() {
		return shop_x;
	}
	public void setShop_x(float shop_x) {
		this.shop_x = shop_x;
	}
	public float getShop_y() {
		return shop_y;
	}
	public void setShop_y(float shop_y) {
		this.shop_y = shop_y;
	}
	public String getShop_name() {
		return shop_name;
	}
	public void setShop_name(String shop_name) {
		this.shop_name = shop_name;
	}
	public String getShop_introduction() {
		return shop_introduction;
	}
	public void setShop_introduction(String shop_introduction) {
		this.shop_introduction = shop_introduction;
	}
	public String getShop_logo() {
		return shop_logo;
	}
	public void setShop_logo(String shop_logo) {
		this.shop_logo = shop_logo;
	}
	public int getShop_grade() {
		return shop_grade;
	}
	public void setShop_grade(int shop_grade) {
		this.shop_grade = shop_grade;
	}
	
	
	
	
	
}
