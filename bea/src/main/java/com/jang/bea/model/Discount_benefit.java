package com.jang.bea.model;

public class Discount_benefit {

	private int benefit_number;
	private String benefit_name;
	private String benefit_content;
	private double benefit_price;
	private String benefit_startdate;
	private String benefit_enddate;
	
	
	public int getBenefit_number() {
		return benefit_number;
	}
	public void setBenefit_number(int benefit_number) {
		this.benefit_number = benefit_number;
	}
	public String getBenefit_name() {
		return benefit_name;
	}
	public void setBenefit_name(String benefit_name) {
		this.benefit_name = benefit_name;
	}
	public String getBenefit_content() {
		return benefit_content;
	}
	public void setBenefit_content(String benefit_content) {
		this.benefit_content = benefit_content;
	}
	public double getBenefit_price() {
		return benefit_price;
	}
	public void setBenefit_price(double benefit_price) {
		this.benefit_price = benefit_price;
	}
	public String getBenefit_startdate() {
		return benefit_startdate;
	}
	public void setBenefit_startdate(String benefit_startdate) {
		this.benefit_startdate = benefit_startdate;
	}
	public String getBenefit_enddate() {
		return benefit_enddate;
	}
	public void setBenefit_enddate(String benefit_enddate) {
		this.benefit_enddate = benefit_enddate;
	}
	
	
	
}
