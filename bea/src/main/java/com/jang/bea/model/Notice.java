package com.jang.bea.model;

public class Notice {
	
	private int notice_number;
	private int shop_number;
	private int notice_important;
	private String notice_title;
	private String notice_content;
	private String notice_date;
	
	
	public int getNotice_number() {
		return notice_number;
	}
	public void setNotice_number(int notice_number) {
		this.notice_number = notice_number;
	}
	
	public int getShop_number() {
		return shop_number;
	}
	public void setShop_number(int shop_number) {
		this.shop_number = shop_number;
	}
	public int getNotice_important() {
		return notice_important;
	}
	public void setNotice_important(int notice_important) {
		this.notice_important = notice_important;
	}
	public String getNotice_title() {
		return notice_title;
	}
	public void setNotice_title(String notice_title) {
		this.notice_title = notice_title;
	}
	public String getNotice_content() {
		return notice_content;
	}
	public void setNotice_content(String notice_content) {
		this.notice_content = notice_content;
	}
	public String getNotice_date() {
		return notice_date;
	}
	public void setNotice_date(String notice_date) {
		this.notice_date = notice_date;
	}
	
	

}
