package com.jang.bea.model;

public class Postscript {
	
	private int postscript_number;
	private int shop_number;
	private int member_phone;
	private int postscript_grade;
	private String postscript_date;
	private String postscript_content;
	private String postscript_picture;
	
	
	public int getPostscript_number() {
		return postscript_number;
	}
	public void setPostscript_number(int postscript_number) {
		this.postscript_number = postscript_number;
	}
	public int getShop_number() {
		return shop_number;
	}
	public void setShop_number(int shop_number) {
		this.shop_number = shop_number;
	}

	

	public int getMember_phone() {
		return member_phone;
	}
	public void setMember_phone(int member_phone) {
		this.member_phone = member_phone;
	}
	public int getPostscript_grade() {
		return postscript_grade;
	}
	public void setPostscript_grade(int postscript_grade) {
		this.postscript_grade = postscript_grade;
	}

	public String getPostscript_date() {
		return postscript_date;
	}
	public void setPostscript_date(String postscript_date) {
		this.postscript_date = postscript_date;
	}
	public String getPostscript_content() {
		return postscript_content;
	}
	public void setPostscript_content(String postscript_content) {
		this.postscript_content = postscript_content;
	}
	public String getPostscript_picture() {
		return postscript_picture;
	}
	public void setPostscript_picture(String postscript_picture) {
		this.postscript_picture = postscript_picture;
	}
	
	

}
